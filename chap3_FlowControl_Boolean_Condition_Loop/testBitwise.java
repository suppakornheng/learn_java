public class testBitwise{
	public static void main(String[] args){
		byte a=14, b=7;
		System.out.println("a&b = "+(a&b));
		System.out.println("a|b = "+(a|b));
		System.out.println("a^b = "+(a^b));
		System.out.println("~a = "+(~a));
		System.out.println("b>>1 = "+(b>>1));
		
		System.out.println("b<<1 = "+(b<<1));
		
	}
}